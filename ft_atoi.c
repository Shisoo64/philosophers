int	ft_atoi(const char *nptr)
{
	int	nbr;
	int	i;

	i = 0;
	nbr = 0;
	while (nptr[i])
	{
		if (!(nptr[i] >= '0' && nptr[i] <= '9'))
			return (-1);
		nbr = nbr * 10 + (nptr[i++] - '0');
	}
	return (nbr);
}
